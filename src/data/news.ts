// ─── NEWS DATA ────────────────────────────────────────────────────────────────
// To add a new article: copy the GOLD UPDATE TEMPLATE at the bottom.
// Images go in: public/images/news/

export interface GoldStats {
  pricePerGram: number;    // MNT/gram e.g. 516765
  change24h?: number;      // % e.g. -0.84  (negative = red, positive = green)
  change7d?: number;       // % e.g. -3.07
  changeMonth?: number;    // % e.g. +5.2
  weekHigh?: number;       // highest MNT/gram in the week
  weekLow?: number;        // lowest  MNT/gram in the week
  outlook?: 'bullish' | 'bearish' | 'neutral';
}

export interface NewsArticle {
  id: string;
  slug: string;
  category: string;
  date: string;            // 'YYYY-MM-DD'
  title: string;
  summary: string;         // shown on listing card
  coverImage: string;      // path relative to public/
  images: string[];        // body image gallery
  body: string;            // full text (\n\n = paragraph break, **text** = bold section header)
  tags: string[];
  source?: string;
  goldStats?: GoldStats;   // present on "Ханшийн Тойм" articles → shows price widget
}

